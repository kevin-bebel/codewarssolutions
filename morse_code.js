decodeMorse = function (morseCode) {
    //your code here
    let sentence = [];
    let words = morseCode.split('   ');
    for (let i = 0; i < words.length; i++) {
        if(words[i] == '') continue;
        let chars = words[i].split(' ');
        let word = '';
        for (let n = 0; n < chars.length; n++) {
            if(chars[n] == '') continue;
            word += MORSE_CODE[chars[n]]
        }
        sentence.push(word)
    }

    return sentence.join(' ')
}

